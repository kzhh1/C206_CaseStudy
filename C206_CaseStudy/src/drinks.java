
public class drinks {
    private String Bandung;
    private String Coffee;
    private String Milo;
    
	public drinks(String bandung, String coffee, String milo) {
		super();
		Bandung = bandung;
		Coffee = coffee;
		Milo = milo;
	}

	public String getBandung() {
		return Bandung;
	}

	public String getCoffee() {
		return Coffee;
	}

	public String getMilo() {
		return Milo;
	}
}
